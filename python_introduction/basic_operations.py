# This is a simple Python program to demonstrate basic arithmetic operations.

number1 = 10
number2 = 5

sum = number1 + number2
difference = number2 - number1
product = number1 * number2
quotient = number1 / number2

print(f"Addition of " + str(number1) + " and " + str(number2) + " is: ", sum)
print(f"Subtraction of " + str(number1) + " and " + str(number2) + " is: ", difference)
print(f"Multiplication of " + str(number1) + " and " + str(number2) + " is: ", product)
print(f"Division of " + str(number1) + " and " + str(number2) + " is: ", quotient)

    